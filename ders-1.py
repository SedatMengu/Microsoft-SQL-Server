# veri tabanı işlermleri : 


# MySQL: Açık kaynaklı bir ilişkisel veritabanı yönetim sistemi (RDBMS) olan MySQL, web tabanlı uygulamalar ve çeşitli diğer yazılım projeleri arasında yaygın olarak kullanılır.

# PostgreSQL: Açık kaynaklı bir nesne-tabanlı ilişkisel veritabanı yönetim sistemi olan PostgreSQL, geniş özellik seti ve güçlü veri bütünlüğüyle bilinir. 
#             Özellikle büyük ve karmaşık projeler için tercih edilir.

# Microsoft SQL Server: Microsoft tarafından geliştirilen ilişkisel veritabanı yönetim sistemi, Windows tabanlı uygulamalar ve büyük işletmeler arasında yaygın olarak kullanılır.

# Oracle Database: Oracle Corporation tarafından geliştirilen Oracle Database, özellikle büyük işletmeler ve kurumsal düzeydeki uygulamalar için tercih edilen bir veritabanıdır.

# MongoDB: Açık kaynaklı, belge tabanlı bir NoSQL veritabanı olan MongoDB, ölçeklenebilir ve esnek yapısı sayesinde büyük miktarda veri işleme ve depolama ihtiyacı olan modern uygulamalarda sıklıkla kullanılır.

# SQLite: Küçük, hafif ve yerel olarak çalışan bir veritabanı motoru olan SQLite, gömülü sistemler, mobil uygulamalar ve basit veri depolama gereksinimleri için tercih edilir.

# Microsoft Access: Microsoft Office paketinin bir parçası olan Microsoft Access, küçük ölçekli projeler ve kişisel veri yönetimi için kullanılan popüler bir veritabanıdır.

# MariaDB: MySQL'in açık kaynaklı bir dalı olan MariaDB, performans iyileştirmeleri ve yeni özelliklerle MySQL ile uyumluluğu sürdürür.

# Redis: Açık kaynaklı bir anahtar-değer depolama sistemi olan Redis, hızlı ve ölçeklenebilir veri saklama ihtiyacı olan uygulamalarda kullanılır.

# Cassandra: Apache Cassandra, ölçeklenebilir ve yüksek performanslı dağıtık NoSQL veritabanı olarak tercih edilir.
#            Özellikle büyük miktarda yapılandırılmamış veriyi depolama ihtiyacı olan uygulamalar için uygundur.

# Popülerlik sıralaması zamanla değişebilir ve yeni veritabanları da geliştirilmeye devam edebilir, bu nedenle güncel verilere göz atmanız faydalı olacaktır.


